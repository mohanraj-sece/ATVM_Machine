    public final void tableDetails(){
        ArrayList<AddTrain> list= train.allDetails();
        DefaultTableModel d=(DefaultTableModel)jTable1.getModel();
        d.setRowCount(0);
        for(AddTrain t:list){
            d.addRow(new Object[]{t.getNumber(),t.getName(),t.getStartPlace(),t.getDestinationPlace(),t.getPrice()});
        }
    }
