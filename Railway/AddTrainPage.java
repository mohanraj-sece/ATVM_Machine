    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        // TODO add your handling code here:
        String trainNo=tNo.getText();
        
        train.setNo(Integer.parseInt(trainNo));
        
        int send=train.deleteTrainDetails();
        if(send==1){
            JOptionPane.showMessageDialog(this, "Record Deleted Successfully");
        }
        else{
            JOptionPane.showMessageDialog(this, "Record Failed");
        }
        this.tableDetails();
        tNo.setText("");
        tName.setText("");
        tsPlace.setText("");
        tdPlace.setText("");
        tPrice.setText("");
        AddtrainButton.setEnabled(true);
        
    }//GEN-LAST:event_DeleteButtonActionPerformed

    private void AddtrainButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddtrainButtonActionPerformed
        // TODO add your handling code here:
        String trainNo=tNo.getText();
        String trainName=tName.getText();
        String trainStart=tsPlace.getText();
        String trainDest=tdPlace.getText();
        String trainPrice=tPrice.getText();
       
        train.setNo(Integer.parseInt(trainNo));
        train.setName(trainName);
        train.setStart(trainStart);
        train.setDestination(trainDest);
        train.setPrice(Integer.parseInt(trainPrice));
        
        int send=train.addTrainDetail();
        if(send==1){
            JOptionPane.showMessageDialog(this, "Record Added Successfully");
       
        }
        else{
            JOptionPane.showMessageDialog(this, "Record Failed");
        }
        this.tableDetails();
        tNo.setText("");
        tName.setText("");
        tsPlace.setText("");
        tdPlace.setText("");
        tPrice.setText("");
        
    }//GEN-LAST:event_AddtrainButtonActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel d=(DefaultTableModel)jTable1.getModel();
        int selectIndex=jTable1.getSelectedRow();
        
        tNo.setText(d.getValueAt(selectIndex,0).toString());
        tName.setText(d.getValueAt(selectIndex,1).toString());
        tsPlace.setText(d.getValueAt(selectIndex,2).toString());
        tdPlace.setText(d.getValueAt(selectIndex,3).toString());
        tPrice.setText(d.getValueAt(selectIndex,4).toString());
        
        AddtrainButton.setEnabled(false);
    }//GEN-LAST:event_jTable1MouseClicked

