package RailwayDAO;

/**
 *
 * @author MOHANRAJ
 */
public class Reservation {
    private String passengerNo;
    private String startPlace;
    private String endPlace;
    private String trainNo;
    private String trainName;
    private String price;
    private String date;
    private String tickets;
    private String total;

    /**
     * @return the passengerNo
     */
    public String getPassengerNo() {
        return passengerNo;
    }

    /**
     * @param passengerNo the passengerNo to set
     */
    public void setPassengerNo(String passengerNo) {
        this.passengerNo = passengerNo;
    }

    /**
     * @return the startPlace
     */
    public String getStartPlace() {
        return startPlace;
    }

    /**
     * @param startPlace the startPlace to set
     */
    public void setStartPlace(String startPlace) {
        this.startPlace = startPlace;
    }

    /**
     * @return the endPlace
     */
    public String getEndPlace() {
        return endPlace;
    }

    /**
     * @param endPlace the endPlace to set
     */
    public void setEndPlace(String endPlace) {
        this.endPlace = endPlace;
    }

    /**
     * @return the trainNo
     */
    public String getTrainNo() {
        return trainNo;
    }

    /**
     * @param trainNo the trainNo to set
     */
    public void setTrainNo(String trainNo) {
        this.trainNo = trainNo;
    }

    /**
     * @return the trainName
     */
    public String getTrainName() {
        return trainName;
    }

    /**
     * @param trainName the trainName to set
     */
    public void setTrainName(String trainName) {
        this.trainName = trainName;
    }

    /**
     * @return the price
     */
    public String getPrice() {
        return price;
    }

    /**
     * @param price the price to set
     */
    public void setPrice(String price) {
        this.price = price;
    }

    /**
     * @return the date
     */
    public String getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(String date) {
        this.date = date;
    }

    /**
     * @return the tickets
     */
    public String getTickets() {
        return tickets;
    }

    /**
     * @param tickets the tickets to set
     */
    public void setTickets(String tickets) {
        this.tickets = tickets;
    }

    /**
     * @return the total
     */
    public String getTotal() {
        return total;
    }

    /**
     * @param total the total to set
     */
    public void setTotal(String total) {
        this.total = total;
    }
    
    
    
}
